async function generateSamplePdf(): Promise<Uint8Array> {
  // pdf-lib の StandardFonts は Latin1 しか対応していないため、
  // 本文は英語のプレースホルダー固定にする。
  // 実台本のアップロードフロー（P2-2 Step6）が入った時点で日本語フォント対応の本物 PDF に置き換わる。
  const pdf = await PDFDocument.create();
  const font = await pdf.embedFont(StandardFonts.Helvetica);
  const totalPages = 10;
  for (let i = 1; i <= totalPages; i += 1) {
    const page = pdf.addPage([595, 842]); // A4
    const { height } = page.getSize();
    page.drawText("Script Marketplace - Sample Script", {
      x: 50,
      y: height - 80,
      size: 18,
      font,
    });
    page.drawText(`Page ${i} / ${totalPages}`, { x: 50, y: height - 110, size: 12, font });
    page.drawText(
      "This is a placeholder PDF generated by the seed script.",
      { x: 50, y: height - 150, size: 11, font },
    );
    page.drawText(
      "Replace with an actual script for production use.",
      { x: 50, y: height - 168, size: 11, font },
    );
    page.drawText(`Scene ${i} - Sample dialogue line 1:`, {
      x: 50,
      y: height - 220,
      size: 10,
      font,
    });
    page.drawText("Lorem ipsum dolor sit amet, consectetur adipiscing elit.", {
      x: 70,
      y: height - 240,
      size: 10,
      font,
    });
    page.drawText(`Scene ${i} - Sample dialogue line 2:`, {
      x: 50,
      y: height - 280,
      size: 10,
      font,
    });
    page.drawText("Sed do eiusmod tempor incididunt ut labore et dolore.", {
      x: 70,
      y: height - 300,
      size: 10,
      font,
    });
  }
  return pdf.save();
}

async function uploadPdf(scriptId: string, pdfBytes: Uint8Array): Promise<string> {
  const path = `scripts/${scriptId}/v1/script.pdf`;
  const file = bucket.file(path);
  await file.save(Buffer.from(pdfBytes), {
    contentType: "application/pdf",
    resumable: false,
  });
  return path;
}

