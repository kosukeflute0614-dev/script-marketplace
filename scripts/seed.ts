// ----------------------------------------------------------------------------
// 既存 seed データの掃除（冪等化のため）
// ----------------------------------------------------------------------------
async function deleteCollection(db: Firestore, path: string, batchSize = 100) {
  const collectionRef = db.collection(path);
  const snapshot = await collectionRef.limit(batchSize).get();
  if (snapshot.empty) return;
  const batch = db.batch();
  snapshot.docs.forEach((doc) => batch.delete(doc.ref));
  await batch.commit();
  if (snapshot.size === batchSize) {
    await deleteCollection(db, path, batchSize);
  }
}

async function cleanupExistingSeed() {
  console.log("[seed] cleaning up existing seed data...");
  // チャット: messages サブコレクションを先に消す
  for (const id of ["seed-chat-1", "seed-chat-2", "seed-chat-3"]) {
    await deleteCollection(db, `chats/${id}/messages`);
    await db.collection("chats").doc(id).delete().catch(() => {});
  }
  // scripts: versions と reviews も
  for (const s of SEED_SCRIPTS) {
    await deleteCollection(db, `scripts/${s.id}/versions`);
    await deleteCollection(db, `scripts/${s.id}/reviews`);
    await db.collection("scripts").doc(s.id).delete().catch(() => {});
  }
  // users: favorites/history/savedSearches サブコレクション
  for (const u of SEED_USERS) {
    await deleteCollection(db, `users/${u.uid}/favorites`);
    await deleteCollection(db, `users/${u.uid}/history`);
    await deleteCollection(db, `users/${u.uid}/savedSearches`);
    await db.collection("users").doc(u.uid).delete().catch(() => {});
    await db.collection("userIds").doc(u.userId).delete().catch(() => {});
  }
  // 単発ドキュメント
  for (const id of ["seed-consultation-1", "seed-consultation-2", "seed-consultation-3"]) {
    await db.collection("consultations").doc(id).delete().catch(() => {});
  }
  for (const id of ["seed-invoice-1", "seed-invoice-2"]) {
    await db.collection("invoices").doc(id).delete().catch(() => {});
  }
  for (const id of ["seed-purchase-1", "seed-purchase-2"]) {
    await db.collection("purchases").doc(id).delete().catch(() => {});
  }
  console.log("[seed] cleanup done");
}

