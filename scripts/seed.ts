async function seedConfig(db: Firestore) {
  await db.collection("config").doc("platform").set({
    feeRate: 0.165,
    payoutFee: 250,
    autoPayoutDay: 15,
    minPayoutAmount: 1000,
    badgeDefinitions: [],
    scriptTagDefinitions: SCRIPT_TAG_DEFINITIONS,
    topPageSections: INITIAL_TOP_PAGE_SECTIONS,
    genres: GENRES,
    performanceTypes: PERFORMANCE_TYPES,
    targetAudiences: TARGET_AUDIENCES,
    updatedAt: FieldValue.serverTimestamp(),
  });
  console.log("[seed] config/platform written");
}

async function seedUsers(db: Firestore) {
  const batch = db.batch();
  for (const u of SEED_USERS) {
    const ref = db.collection("users").doc(u.uid);
    batch.set(ref, {
      uid: u.uid,
      email: u.email,
      displayName: u.displayName,
      userId: u.userId,
      bio: u.bio ?? "",
      stripeOnboarded: u.isAuthor,
      isAdmin: false,
      hearingSheet: u.isAuthor ? DEFAULT_HEARING_SHEET_TEMPLATE : [],
      notificationSettings: { ...DEFAULT_NOTIFICATION_SETTINGS },
      createdAt: FieldValue.serverTimestamp(),
      updatedAt: FieldValue.serverTimestamp(),
    });
    // userIds 補助索引
    batch.set(db.collection("userIds").doc(u.userId), {
      uid: u.uid,
      createdAt: FieldValue.serverTimestamp(),
    });
  }
  await batch.commit();
  console.log(`[seed] users written: ${SEED_USERS.length}`);
}

async function seedScripts(db: Firestore) {
  // PDF を 1 つだけ生成して全 script で共有（容量節約）
  const samplePdf = await generateSamplePdf();
  console.log(`[seed] sample PDF generated (${samplePdf.length} bytes)`);

  for (const [idx, s] of SEED_SCRIPTS.entries()) {
    const author = SEED_USERS[s.authorIdx];
    const pdfPath = await uploadPdf(s.id, samplePdf);

    // thumbnailUrl: 一部のスクリプトに公開ダミー URL を設定（後続の表示テスト用）
    // 実台本の出品フロー（P2-2 Step6）が入った時点で Storage 経由に置き換える。
    const thumbnailUrl =
      idx % 2 === 0
        ? `https://placehold.co/600x400/F5F3EE/333333?text=${encodeURIComponent(s.title)}`
        : "";

    const docData: Record<string, unknown> = {
      id: s.id,
      authorUid: author.uid,
      authorUserId: author.userId,
      authorDisplayName: author.displayName,
      title: s.title,
      slug: s.slug,
      synopsis: s.synopsis,
      genres: s.genres,
      castTotal: { min: s.cast.min, max: s.cast.max },
      castBreakdown: {
        male: s.cast.male,
        female: s.cast.female,
        unspecified: s.cast.unspecified,
      },
      duration: s.duration,
      performanceType: s.performanceType,
      targetAudience: s.targetAudience ?? [],
      themeTags: s.themeTags ?? [],
      price: s.price,
      isFreeFullText: s.isFreeFullText ?? false,
      thumbnailUrl,
      pdfUrl: pdfPath,
      currentVersion: 1,
      feeSchedule: s.feeSchedule ?? [],
      // feeScheduleMin は schedule がある時のみフィールドを書き込む。
      // null を書くと Algolia の数値ファセットで意図しない挙動になりうる。
      ...(s.feeSchedule && s.feeSchedule.length > 0
        ? { feeScheduleMin: Math.min(...s.feeSchedule.map((f) => f.amount)) }
        : {}),
      performanceHistory: s.performanceHistory ?? [],
      authorComment: s.authorComment ?? "",
      hearingSheet: [],
      scriptTags: s.scriptTags ?? [],
      badges: [],
      rankings: {},
      status: "published",
      stats: { ...INITIAL_SCRIPT_STATS },
      createdAt: FieldValue.serverTimestamp(),
      updatedAt: FieldValue.serverTimestamp(),
    };

    const scriptRef = db.collection("scripts").doc(s.id);
    await scriptRef.set(docData);

    await scriptRef.collection("versions").doc("1").set({
      version: 1,
      pdfUrl: pdfPath,
      createdAt: FieldValue.serverTimestamp(),
    });
  }
  console.log(`[seed] scripts written: ${SEED_SCRIPTS.length}`);
}

