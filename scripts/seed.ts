/**
 * 開発用の seed スクリプト。
 *
 * 実行: `npx tsx scripts/seed.ts`
 *
 * Pass 1 の各機能を実データで動作確認するため、Firestore と Storage に
 * 開発用のサンプルデータを冪等に投入する。
 *
 * 投入内容（spec.md §5 / spec-details.md §3 準拠）:
 * - users: 5件（作家3 + 購入者2）。`stripeOnboarded: true` を作家に設定
 * - scripts: 8件（ジャンル/キャスト/価格を分散、無料1件、全文無料1件）
 *   - 各 scripts の versions/1 サブドキュメント
 *   - サンプル PDF（pdf-lib で生成）を Storage の `scripts/{id}/v1/script.pdf` にアップロード
 * - chats: 3件（messages サブコレクションに数件のメッセージ）
 * - consultations: 3件（status を未対応/対応中/完了に分散）
 * - invoices: 2件（pending と paid）
 * - purchases: 2件（無料 + 有料）
 * - users/{uid}/favorites, history: 数件
 * - userIds: 各 user の userId に対応する補助索引
 * - config/platform: feeRate 等 + scriptTagDefinitions（26タグ）+ topPageSections
 *
 * 冪等性: 何度実行しても同じ状態になる。固定 ID で set/upsert する。
 *
 * 安全装置: NEXT_PUBLIC_FIREBASE_PROJECT_ID が "script-marcketplace" でなければ throw。
 */

